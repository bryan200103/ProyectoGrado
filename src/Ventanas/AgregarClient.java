    private void txt_actualizarBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txt_actualizarBtnMouseClicked
        Cliente cliente = new Cliente();
        ControladorCliente controladorCliente = new ControladorCliente();
        EncrypPassw encryp = new EncrypPassw();

        if (!txt_nombre.getText().isEmpty() && !txt_direccion.getText().isEmpty() && !txt_correo.getText().isEmpty() && !txt_cedula.getText().isEmpty() && !txt_telefono.getText().isEmpty()) {
            //JOptionPane.showMessageDialog(null, "guardado");

            if (!controladorCliente.clienteExiste(txt_cedula.getText().trim())) {
                
                    cliente.setNombre(txt_nombre.getText().trim());
                    cliente.setCedula(txt_cedula.getText().trim());
                    cliente.setCorreo(txt_correo.getText().trim());
                    cliente.setDireccion(txt_direccion.getText().trim());
                    cliente.setTelefono(txt_telefono.getText().trim());
                    if (controladorCliente.guardar(cliente)) {
                        txt_nombre.setBackground(Color.GREEN);
                        txt_direccion.setBackground(Color.GREEN);
                        txt_correo.setBackground(Color.GREEN);
                        txt_cedula.setBackground(Color.GREEN);
                        txt_telefono.setBackground(Color.GREEN);
                        JOptionPane.showMessageDialog(null, "Registro exitoso");
                        usuarioG.CargarTablaCliente();
                        txt_nombre.setBackground(Color.white);
                        txt_direccion.setBackground(Color.white);
                        txt_cedula.setBackground(Color.white);
                        txt_correo.setBackground(Color.white);
                        txt_telefono.setBackground(Color.white);

                    } else {
                        JOptionPane.showMessageDialog(null, "Error al guardar");

                    }
                

            } else {
                txt_nombre.setBackground(Color.yellow);
                txt_direccion.setBackground(Color.yellow);
                txt_cedula.setBackground(Color.yellow);
                txt_correo.setBackground(Color.yellow);
                txt_telefono.setBackground(Color.yellow);
                JOptionPane.showMessageDialog(null, "El Usuario ya existe");
                txt_nombre.setBackground(Color.white);
                txt_direccion.setBackground(Color.white);
                txt_cedula.setBackground(Color.white);
                txt_correo.setBackground(Color.white);
                txt_telefono.setBackground(Color.white);

            }

        } else {
            txt_nombre.setBackground(Color.red);
            txt_direccion.setBackground(Color.red);
            txt_cedula.setBackground(Color.red);
            txt_correo.setBackground(Color.red);
            txt_telefono.setBackground(Color.red);
            JOptionPane.showMessageDialog(null, "complete campos obligatorios");
            txt_nombre.setBackground(Color.white);
            txt_direccion.setBackground(Color.white);
            txt_cedula.setBackground(Color.white);
            txt_correo.setBackground(Color.white);
            txt_telefono.setBackground(Color.white);
        }

        //llamamos al metodo limpiar 
        Limpiar();


    }//GEN-LAST:event_txt_actualizarBtnMouseClicked


