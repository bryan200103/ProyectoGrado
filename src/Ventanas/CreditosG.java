public void CargarTablaCredito() {
        Connection con = Conexionmy.Conectar();
        DefaultTableModel model = new DefaultTableModel();
        String sql = "SELECT c.id_credito, cl.nombre, cl.cedula, c.monto, c.interes, c.cuotas, c.valor_cuota, c.monto_total,"
                + "c.saldo_pendiente, c.fecha_inicio, c.fecha_fin, c.estado" +
                 " FROM creditos c " +
                 "INNER JOIN clientes cl ON c.id_cliente = cl.id_cliente;";
        Statement st;

        try {
            st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);

            model.addColumn("NÂ°");
            model.addColumn("Nombre");
            model.addColumn("Cedula");
            model.addColumn("Monto");
            model.addColumn("Interes");
            model.addColumn("Cuotas");
            model.addColumn("Valor cuota");
            model.addColumn("Monto total");
            model.addColumn("Saldo Pendiente");
            model.addColumn("Fecha inicio");
            model.addColumn("Fecha fin");
            model.addColumn("Estado");

            while (rs.next()) {

                Object fila[] = new Object[12];
                for (int i = 0; i < 12; i++) {
                    fila[i] = rs.getObject(i + 1);

                }

                model.addRow(fila);
            }
            con.close();

        } catch (SQLException e) {
            System.out.println("Error al llenar tabla Creditos" + e);
        }
        jTable_creditos.setModel(model);
        

        jTable_creditos.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int fila_point = jTable_creditos.rowAtPoint(e.getPoint());
                int columna_point = 0;
                int selectedRow = jTable_creditos.getSelectedRow();

                if (fila_point >= 0) {
                    id_credito = (int) jTable_creditos.getValueAt(selectedRow, 0);
//                    act.PasaridCliente(id_cliente);
//                    act.EnviarDatosClienteSeleccionada(id_cliente);
//                    
                    //EnviarDatosClienteSeleccionada(idCliente);

                }
            }
        });
    }

