    private void txt_actualizarBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txt_actualizarBtnMouseClicked
        try {
            if (txt_nombre.getText().isEmpty() && txt_direccion.getText().isEmpty()
                    && txt_telefono.getText().isEmpty() && txt_cedula.getText().isEmpty() && txt_correo.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "los campos estan vacios");

            } else {
                Cliente cliente = new Cliente();
                cliente.setNombre(txt_nombre.getText().trim());
                cliente.setCedula(txt_cedula.getText().trim());
                cliente.setDireccion(txt_direccion.getText().trim());
                cliente.setTelefono(txt_telefono.getText().trim());
                cliente.setCorreo(txt_correo.getText().trim());

            if (ctr.actualizar(cliente,idUsuario)) {
                JOptionPane.showMessageDialog(null, "Actualizado extiosamente");
                clienteG.CargarTablaCliente();
                Limpiar();

            } else {
                JOptionPane.showMessageDialog(null, "Error al actualizar, cliente ya esxiste");

            }
            }
        } catch (Exception ex) {
            System.out.println("error" + ex);

        }
        

    }//GEN-LAST:event_txt_actualizarBtnMouseClicked


