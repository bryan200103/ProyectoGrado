    public void EnviarDatosClienteSeleccionada(int idUsuario) {
        try {
            Connection con = Conexionmy.Conectar();
            PreparedStatement pst = con.prepareStatement("select * from clientes where id_cliente =?");
            //ResultSet rs = pst.executeQuery();
            pst.setInt(1, idUsuario);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                txt_nombre.setText(rs.getString("nombre"));
                txt_cedula.setText(rs.getString("cedula"));
                txt_direccion.setText(rs.getString("direccion"));
                txt_telefono.setText(rs.getString("telefono"));
                txt_correo.setText(rs.getString("correo"));

            }
            con.close();

        } catch (SQLException e) {
            System.out.println("Error al cargar cliente" + e);
        }
    }

