    private void txt_actualizarBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txt_actualizarBtnMouseClicked
        Usuario usuario = new Usuario();
        ControladorUsuario controladorUsuario = new ControladorUsuario();
        EncrypPassw encryp = new EncrypPassw();

        if (!txt_nombre.getText().isEmpty() && !txt_cedula1.getText().isEmpty() && !txt_usuario.getText().isEmpty() && !txt_clave.getText().isEmpty() && !txt_rol.getText().isEmpty()) {
            //JOptionPane.showMessageDialog(null, "guardado");

            if (!controladorUsuario.UsuarioExiste(txt_cedula1.getText().trim())) {
                if (!controladorUsuario.usuarioExistePorUserLogin(txt_usuario.getText().trim())) {
                    usuario.setNombre(txt_nombre.getText().trim());
                    usuario.setCedula(txt_cedula1.getText().trim());
                    usuario.setUser(txt_usuario.getText().trim());
                    String pass = encryp.encriptarSHA256(txt_clave.getText().trim());
                    usuario.setClave(pass);
                    usuario.setRol(txt_rol.getText().trim());
                    if (controladorUsuario.guardar(usuario)) {
                        txt_nombre.setBackground(Color.GREEN);
                        txt_cedula1.setBackground(Color.GREEN);
                        txt_usuario.setBackground(Color.GREEN);
                        txt_clave.setBackground(Color.GREEN);
                        txt_rol.setBackground(Color.GREEN);
                        JOptionPane.showMessageDialog(null, "Registro exitoso");
                        usuarioG.CargarTablaUsuario();
                        txt_nombre.setBackground(Color.white);
                        txt_cedula1.setBackground(Color.white);
                        txt_clave.setBackground(Color.white);
                        txt_usuario.setBackground(Color.white);
                        txt_rol.setBackground(Color.white);

                    } else {
                        JOptionPane.showMessageDialog(null, "Error al guardar");

                    }
                } else {
                    txt_nombre.setBackground(Color.yellow);
                    txt_cedula1.setBackground(Color.yellow);
                    txt_clave.setBackground(Color.yellow);
                    txt_usuario.setBackground(Color.yellow);
                    txt_rol.setBackground(Color.yellow);
                    JOptionPane.showMessageDialog(null, "El Usuario ya existe");
                    txt_nombre.setBackground(Color.white);
                    txt_cedula1.setBackground(Color.white);
                    txt_clave.setBackground(Color.white);
                    txt_usuario.setBackground(Color.white);
                    txt_rol.setBackground(Color.white);

                }

            } else {
                txt_nombre.setBackground(Color.yellow);
                txt_cedula1.setBackground(Color.yellow);
                txt_clave.setBackground(Color.yellow);
                txt_usuario.setBackground(Color.yellow);
                txt_rol.setBackground(Color.yellow);
                JOptionPane.showMessageDialog(null, "El Usuario ya existe");
                txt_nombre.setBackground(Color.white);
                txt_cedula1.setBackground(Color.white);
                txt_clave.setBackground(Color.white);
                txt_usuario.setBackground(Color.white);
                txt_rol.setBackground(Color.white);

            }

        } else {
            txt_nombre.setBackground(Color.red);
            txt_cedula1.setBackground(Color.red);
            txt_clave.setBackground(Color.red);
            txt_usuario.setBackground(Color.red);
            txt_rol.setBackground(Color.red);
            JOptionPane.showMessageDialog(null, "complete campos obligatorios");
            txt_nombre.setBackground(Color.white);
            txt_cedula1.setBackground(Color.white);
            txt_clave.setBackground(Color.white);
            txt_usuario.setBackground(Color.white);
            txt_rol.setBackground(Color.white);
        }

        //llamamos al metodo limpiar 
        Limpiar();


    }//GEN-LAST:event_txt_actualizarBtnMouseClicked


