    public void calcularCredito() {
        try {
            // 1. Obtener datos
            double monto = Double.parseDouble(txt_saldoPendienteAtrasado.getText());
            Double interes = Double.parseDouble((String) cbx_interes.getSelectedItem());
            
            int cuotas = (Integer) jSpinner_cuotas.getValue();
            if(cuotas == 0){
                JOptionPane.showMessageDialog(null, "las cuotas no pueden ser 0");
                return ;
            }

            // 2. Calcular total con inter√©s
            double totalConInteres = monto + (monto * interes / 100);

            // 3. Calcular valor de cada cuota
            double valorCuota = totalConInteres / cuotas;

            // 4. Asignar valores a los JTextField
            txt_totalrefinanciado.setText(String.format("%.2f", totalConInteres));
            txt_newCuota.setText(String.format("%.2f", valorCuota));
            calcularFechaFin();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Revisa los valores ingresados: " + e);
        }

    }
    
    
    
    public String calcularFechaFin() {
    DateTimeFormatter formatear = DateTimeFormatter.ofPattern("yyyy-MM-dd");
    String tipoCuota = (String) jComboBox1_tipoCred.getSelectedItem();
    int cuotas = (Integer) jSpinner_cuotas .getValue();
    
    LocalDate fechaInicio;
    
    try {
        fechaInicio = LocalDate.parse(txt_fecharegRef.getText().trim(), formatear);
        System.out.println(fechaInicio);
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Formato de fecha de inicio inv√°lido");
        return "";
    }

    LocalDate fechaFin = fechaInicio;

    // Calcular fecha de fin seg√∫n tipo de cuota
    switch (tipoCuota) {
        case "Diario":
            fechaFin = fechaInicio.plusDays(cuotas);
            break;
        case "Semanal":
            fechaFin = fechaInicio.plusWeeks(cuotas);
            break;
        case "Mensual":
            fechaFin = fechaInicio.plusMonths(cuotas);
            break;
        default:
            System.out.println("Tipo de cuota no reconocido, usando fecha inicio como fecha fin.");
    }

    // Mostrar fecha fin en el txt correspondiente
    String fechaFinStr = fechaFin.format(formatear);
    txt_fechaFinRef.setText(fechaFinStr);
        System.out.println(txt_fechaFinRef);

    return fechaFinStr;
}
