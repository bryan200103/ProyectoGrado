//Cargar la tabla de usuarios registrados
    public void CargarTablaUsuario() {
        Connection con = Conexionmy.Conectar();
        DefaultTableModel model = new DefaultTableModel();
        String sql = "SELECT id_usuario, nombre_usuario, rol, cedula, userlogin FROM usuarios;";
        Statement st;

        try {
            st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);

            model.addColumn("NÂ°");
            model.addColumn("nombre");
            //model.addColumn("clave");
            model.addColumn("rol");
            model.addColumn("cedula");
            model.addColumn("User");

            while (rs.next()) {

                Object fila[] = new Object[5];
                for (int i = 0; i < 5; i++) {
                    fila[i] = rs.getObject(i + 1);

                }

                model.addRow(fila);
            }
            con.close();

        } catch (SQLException e) {
            System.out.println("Error al llenar tabla usuario" + e);
        }
        jTable_usuarios.setModel(model);
        

        jTable_usuarios.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int fila_point = jTable_usuarios.rowAtPoint(e.getPoint());
                int columna_point = 0;
                int selectedRow = jTable_usuarios.getSelectedRow();

                if (fila_point >= 0) {
                    id_cliente = (int) jTable_usuarios.getValueAt(selectedRow, 0);
                    act.PasaridUsuario(id_cliente);
                    act.EnviarDatosClienteSeleccionada(id_cliente);
                    
                    //EnviarDatosClienteSeleccionada(idCliente);

                }
            }
        });
    }

