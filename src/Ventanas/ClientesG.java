//Cargar la tabla de usuarios registrados
    public void CargarTablaCliente() {
        Connection con = Conexionmy.Conectar();
        DefaultTableModel model = new DefaultTableModel();
        String sql = "SELECT * FROM clientes;";
        Statement st;

        try {
            st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);

            model.addColumn("NÂ°");
            model.addColumn("Nombre");
            model.addColumn("cedula");
            model.addColumn("Telefono");
            model.addColumn("Direccion");
            model.addColumn("Correo");

            while (rs.next()) {

                Object fila[] = new Object[6];
                for (int i = 0; i < 6; i++) {
                    fila[i] = rs.getObject(i + 1);

                }

                model.addRow(fila);
            }
            con.close();

        } catch (SQLException e) {
            System.out.println("Error al llenar tabla cliente" + e);
        }
        jTable_cliente.setModel(model);
        

        jTable_cliente.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int fila_point = jTable_cliente.rowAtPoint(e.getPoint());
                int columna_point = 0;
                int selectedRow = jTable_cliente.getSelectedRow();

                if (fila_point >= 0) {
                    id_cliente = (int) jTable_cliente.getValueAt(selectedRow, 0);
                    act.PasaridCliente(id_cliente);
                    act.EnviarDatosClienteSeleccionada(id_cliente);
                    
                    //EnviarDatosClienteSeleccionada(idCliente);

                }
            }
        });
    }

